// gen_v5.cpp — v5 code generator: two-pass serialization mirroring C++
// flat_buffers (Pass 1: PrecomputeSize, Pass 2: WriteToBuffer).
// Compiles against real FDB headers via extract.h. Outputs Go files +
// the ground-truth test vectors (testdata.json) to a directory.
//
// Key differences from v4 (the original GoEmitter):
// - precomputeSize + writeToBuffer two-pass MarshalFDB, 1:1 with C++
//   detail::save / SaveVisitorLambda (flat_buffers.h)
// - No MarshalInto, no WriteXxx helpers, no MarshalXxx helpers
// - ParseXxxStringVector uses sub-slicing instead of make+copy

#include "extract.h"

// ============================================================
// GoEmitterV5 — composable primitives, two-pass direct-write
// ============================================================

struct GoEmitterV5 {
    FILE* f;

    void emitFull(const char* typeName, const std::vector<FieldDesc>& fields,
                  const std::vector<uint16_t>& vt, uint32_t fileId,
                  const std::vector<std::vector<uint16_t>>& closure) {
        // Determine import requirements.
        bool needsBinary = false;
        bool needsMath = false;
        bool isTopLevel = (fileId > 0 && !closure.empty());
        // writeToBuffer always uses binary for scalar writes + soffsets.
        needsBinary = true;

        for (auto& fd : fields) {
            if (fd.kind == FieldKind::Scalar && fd.scalar.goType) {
                if (strcmp(fd.scalar.goType, "uint16") == 0 ||
                    strcmp(fd.scalar.goType, "int16") == 0 ||
                    strcmp(fd.scalar.goType, "uint32") == 0 ||
                    strcmp(fd.scalar.goType, "int32") == 0 ||
                    strcmp(fd.scalar.goType, "uint64") == 0 ||
                    strcmp(fd.scalar.goType, "int64") == 0 ||
                    strcmp(fd.scalar.goType, "float64") == 0)
                    needsBinary = true;
                if (strcmp(fd.scalar.goType, "float64") == 0)
                    needsMath = true;
            }
        }
        // allFieldsDynamicSize types use binary.LittleEndian in ParseXxxStringVector.
        if (allFieldsDynamicSize(fields))
            needsBinary = true;
        // writeBlob uses binary.LittleEndian for DynamicSize and VectorOfStruct OOL writes.
        for (auto& fd : fields) {
            if (fd.kind == FieldKind::DynamicSize || fd.kind == FieldKind::VectorOfStruct || fd.kind == FieldKind::Variant) {
                needsBinary = true;
                break;
            }
        }

        // Header.
        fprintf(f, "// Code generated by fdb-schema-extract v5 from FDB 7.3.75. DO NOT EDIT.\n\n");
        fprintf(f, "package types\n\n");

        if (needsBinary || needsMath) {
            fprintf(f, "import (\n");
            if (needsBinary) fprintf(f, "\t\"encoding/binary\"\n");
            if (needsMath) fprintf(f, "\t\"math\"\n");
            fprintf(f, "\n\t\"github.com/birdayz/fdb-record-layer-go/pkg/fdbgo/wire\"\n");
            fprintf(f, ")\n\n");
        } else {
            fprintf(f, "import \"github.com/birdayz/fdb-record-layer-go/pkg/fdbgo/wire\"\n\n");
        }

        // Slot constants.
        fprintf(f, "const (\n");
        for (auto& fd : fields) {
            auto gn = fieldGoName(fd);
            fprintf(f, "\t%sSlot%s = %d\n", typeName, gn.c_str(), fd.vtableSlot);
        }
        fprintf(f, ")\n\n");

        // VTable.
        fprintf(f, "var %sVTable = wire.VTable{", typeName);
        for (size_t i = 0; i < vt.size(); i++) {
            if (i) fprintf(f, ", ");
            fprintf(f, "%u", vt[i]);
        }
        fprintf(f, "}\n");

        // FileID (+ MaxAlign when adjacent). Interface-style types (FileID but
        // no closure) get one grouped const block; top-level types get separate
        // decls with blank lines between them (the committed gofmt'ed layout —
        // regen must be churn-free against the committed files).
        int maxAlign = computeMaxAlign(fields);
        bool groupedConst = (fileId > 0 && closure.empty());
        if (groupedConst) {
            fprintf(f, "\nconst (\n\t%sFileID uint32 = %u\n\t%sMaxAlign = %d\n)\n",
                    typeName, fileId, typeName, maxAlign);
        } else if (fileId > 0) {
            fprintf(f, "\nconst %sFileID uint32 = %u\n", typeName, fileId);
        }

        // VTable closure.
        if (!closure.empty()) {
            fprintf(f, "\nvar %sVTableClosure = []wire.VTable{\n", typeName);
            for (auto& cvt : closure) {
                fprintf(f, "\t{");
                for (size_t i = 0; i < cvt.size(); i++) {
                    if (i) fprintf(f, ", ");
                    fprintf(f, "%u", cvt[i]);
                }
                fprintf(f, "},\n");
            }
            fprintf(f, "}\n");
        }

        // MessageTemplate.
        if (isTopLevel) {
            fprintf(f, "\nvar %sTemplate = wire.NewMessageTemplate(\n", typeName);
            fprintf(f, "\t%sFileID, %sVTable, %d, %sVTableClosure,\n)\n",
                    typeName, typeName, maxAlign, typeName);
        }

        // MaxAlign constant (unless folded into the grouped const above).
        if (!groupedConst) {
            if (fileId > 0)
                fprintf(f, "\nconst %sMaxAlign = %d\n", typeName, maxAlign);
            else
                fprintf(f, "const %sMaxAlign = %d\n", typeName, maxAlign);
        }

        fprintf(f, "\n");

        // Struct.
        fprintf(f, "type %s struct {\n", typeName);
        for (auto& fd : fields) {
            auto gn = fieldGoName(fd);
            switch (fd.kind) {
            case FieldKind::Scalar:
                fprintf(f, "\t%s %s // slot %d\n", gn.c_str(), fd.scalar.goType, fd.vtableSlot);
                break;
            case FieldKind::DynamicSize:
            case FieldKind::VectorLike:
                fprintf(f, "\t%s []byte // slot %d\n", gn.c_str(), fd.vtableSlot);
                break;
            case FieldKind::VectorOfStruct:
                fprintf(f, "\t%s []%s // slot %d, vector of struct\n", gn.c_str(), fd.elementGoType, fd.vtableSlot);
                break;
            case FieldKind::NestedStruct:
                if (fd.nestedGoType[0])
                    fprintf(f, "\t%s %s // slot %d, nested\n", gn.c_str(), fd.nestedGoType, fd.vtableSlot);
                else
                    fprintf(f, "\t// %s: unregistered nested struct at slot %d\n", gn.c_str(), fd.vtableSlot);
                break;
            case FieldKind::Optional:
                fprintf(f, "\tHas%s bool   // slot %d, optional tag\n", gn.c_str(), fd.vtableSlot);
                if (fd.nestedGoType && fd.nestedGoType[0]) {
                    // Optional<struct>: value is a nested Go struct, not []byte.
                    fprintf(f, "\t%s    %s // slot %d, optional nested value\n", gn.c_str(), fd.nestedGoType, fd.vtableSlot + 1);
                } else {
                    fprintf(f, "\t%s    []byte // slot %d, optional value\n", gn.c_str(), fd.vtableSlot + 1);
                }
                break;
            case FieldKind::Variant:
                fprintf(f, "\t%sTag uint8 // slot %d, variant tag\n", gn.c_str(), fd.vtableSlot);
                for (size_t a = 0; a < fd.variantAlts.size(); a++) {
                    fprintf(f, "\t%sAlt%zu %s // tag=%zu\n",
                            gn.c_str(), a, fd.variantAlts[a].goType, a + 1);
                }
                break;
            }
        }
        fprintf(f, "}\n\n");

        // Types with custom serialize logic (e.g. KeyRangeRef) provide
        // hand-written precomputeSize/writeToBuffer AND
        // UnmarshalFromReader/UnmarshalFDB in *_custom.go. The custom
        // unmarshal must invert any optimization the custom marshal applies
        // (e.g. KeyRangeRef's single-key-range "(end, empty)" trick).
        // Caught by FuzzSplitRangeRequest_RoundTrip 2026-04-25.
        bool hasCustomSerialize = (strcmp(typeName, "KeyRangeRef") == 0);

        if (hasCustomSerialize) {
            // hasCustomSerialize only matches KeyRangeRef today; keep the file
            // name literal until a second custom type exists.
            fprintf(f, "// UnmarshalFromReader / UnmarshalFDB are defined in keyrangeref_custom.go to\n");
            fprintf(f, "// invert the C++ single-key-range serialize optimization. The generator\n");
            fprintf(f, "// (cmd/fdb-schema-extract/main.cpp) skips them when hasCustomSerialize is true.\n\n");
        }

        // UnmarshalFromReader.
        if (!hasCustomSerialize) {
            fprintf(f, "func (m *%s) UnmarshalFromReader(r *wire.Reader) {\n", typeName);
            emitReads(typeName, fields, "r");
            fprintf(f, "}\n\n");

            // UnmarshalFDB.
            fprintf(f, "func (m *%s) UnmarshalFDB(data []byte) error {\n", typeName);
            fprintf(f, "\tr, err := wire.NewReader(data)\n");
            fprintf(f, "\tif err != nil { return err }\n");
            emitReads(typeName, fields, "r");
            fprintf(f, "\treturn nil\n}\n\n");
        }

        // Two-pass methods: precomputeSize + writeToBuffer.
        // NOTE: blobSize/writeBlob/measureEndOff/writeDirect removed — they were
        // dead code (MarshalFDB uses precomputeSize+writeToBuffer exclusively) and
        // contained the empty-vector-reloff bug.
        if (!hasCustomSerialize) {
            emitPrecomputeSize(typeName, fields, maxAlign);
            emitWriteToBuffer(typeName, fields, maxAlign);
        }

        // MarshalFDB — two-pass, top-level only.
        if (isTopLevel) {
            emitMarshalFDB(typeName, fields, maxAlign);
        }

        // FlatBuffers vector parser.
        emitFBVectorParser(typeName);

        // String vector parser (sub-slice variant).
        if (allFieldsDynamicSize(fields)) {
            emitStringVectorParser(typeName, fields);
        }
    }

private:
    static int computeMaxAlign(const std::vector<FieldDesc>& fields) {
        for (auto& fd : fields)
            if (fd.kind == FieldKind::Scalar && fd.size >= 8)
                return 8;
        return 4;
    }

    // ---- UnmarshalFromReader / UnmarshalFDB read logic (same as v4) ----

    void emitReads(const char* typeName, const std::vector<FieldDesc>& fields, const char* rv) {
        for (auto& fd : fields) {
            auto gn = fieldGoName(fd);
            auto slot = std::string(typeName) + "Slot" + gn;
            switch (fd.kind) {
            case FieldKind::Scalar:
                fprintf(f, "\tif %s.FieldPresent(%s) {\n", rv, slot.c_str());
                fprintf(f, "\t\tm.%s = %s.%s(%s)\n", gn.c_str(), rv, fd.scalar.reader, slot.c_str());
                fprintf(f, "\t}\n");
                break;
            case FieldKind::DynamicSize:
            case FieldKind::VectorLike:
                fprintf(f, "\tif %s.FieldPresent(%s) {\n", rv, slot.c_str());
                fprintf(f, "\t\tm.%s = %s.ReadBytes(%s)\n", gn.c_str(), rv, slot.c_str());
                fprintf(f, "\t}\n");
                break;
            case FieldKind::VectorOfStruct:
                fprintf(f, "\tif count, err := %s.ReadVectorCount(%s); err == nil && count > 0 {\n", rv, slot.c_str());
                fprintf(f, "\t\tm.%s = make([]%s, 0, count)\n", gn.c_str(), fd.elementGoType);
                fprintf(f, "\t\tfor i := 0; i < count; i++ {\n");
                fprintf(f, "\t\t\tif elemR, err := %s.ReadVectorElementReader(%s, i); err == nil {\n", rv, slot.c_str());
                fprintf(f, "\t\t\t\tvar elem %s\n", fd.elementGoType);
                fprintf(f, "\t\t\t\telem.UnmarshalFromReader(elemR)\n");
                fprintf(f, "\t\t\t\tm.%s = append(m.%s, elem)\n", gn.c_str(), gn.c_str());
                fprintf(f, "\t\t\t}\n");
                fprintf(f, "\t\t}\n");
                fprintf(f, "\t}\n");
                break;
            case FieldKind::NestedStruct:
                if (fd.nestedGoType[0]) {
                    fprintf(f, "\tif nr, err := %s.ReadNestedReader(%s); err == nil {\n", rv, slot.c_str());
                    fprintf(f, "\t\tm.%s.UnmarshalFromReader(nr)\n", gn.c_str());
                    fprintf(f, "\t}\n");
                }
                break;
            case FieldKind::Optional:
                fprintf(f, "\tif %s.FieldPresent(%s) && %s.ReadUint8(%s) > 0 {\n",
                        rv, slot.c_str(), rv, slot.c_str());
                if (fd.nestedGoType && fd.nestedGoType[0]) {
                    // Optional<struct>: read as nested reader, unmarshal.
                    fprintf(f, "\t\tif nr, err := %s.ReadNestedReader(%s + 1); err == nil {\n", rv, slot.c_str());
                    fprintf(f, "\t\t\tm.%s.UnmarshalFromReader(nr)\n", gn.c_str());
                    fprintf(f, "\t\t}\n");
                } else {
                    fprintf(f, "\t\tm.%s = %s.ReadBytes(%s + 1)\n", gn.c_str(), rv, slot.c_str());
                }
                fprintf(f, "\t\tm.Has%s = true\n", gn.c_str());
                fprintf(f, "\t}\n");
                break;
            case FieldKind::Variant:
                fprintf(f, "\tif %s.FieldPresent(%s) {\n", rv, slot.c_str());
                fprintf(f, "\t\tm.%sTag = %s.ReadUint8(%s)\n", gn.c_str(), rv, slot.c_str());
                fprintf(f, "\t\tswitch m.%sTag {\n", gn.c_str());
                for (size_t a = 0; a < fd.variantAlts.size(); a++) {
                    auto& alt = fd.variantAlts[a];
                    fprintf(f, "\t\tcase %zu:\n", a + 1);
                    if (alt.kind == FieldKind::Scalar && alt.size == 4) {
                        fprintf(f, "\t\t\tm.%sAlt%zu = %s.ReadRelOffUint32(%s + 1)\n",
                                gn.c_str(), a, rv, slot.c_str());
                    } else if (alt.kind == FieldKind::VectorLike || alt.kind == FieldKind::DynamicSize) {
                        // C++ union vector alternative (use_indirection==true,
                        // flat_buffers.h:872-877): the RelativeOffset points at a
                        // count-prefixed vector [count u32][data]. ReadBytes follows the
                        // reloff AND skips the 4-byte count; ReadRelOffRaw would return the
                        // count bytes, not the payload (the latent IPv6 read bug).
                        fprintf(f, "\t\t\tm.%sAlt%zu = %s.ReadBytes(%s + 1)\n",
                                gn.c_str(), a, rv, slot.c_str());
                    } else {
                        fprintf(f, "\t\t\tm.%sAlt%zu = %s.ReadRelOffRaw(%s + 1, %d)\n",
                                gn.c_str(), a, rv, slot.c_str(), alt.size);
                    }
                }
                fprintf(f, "\t\t}\n");
                fprintf(f, "\t}\n");
                break;
            }
        }
    }

    // ---- precomputeSize (Pass 1 of two-pass, C++ SaveVisitorLambda with PrecomputeSize) ----

    void emitPrecomputeSize(const char* typeName, const std::vector<FieldDesc>& fields, int maxAlign) {
        fprintf(f, "// precomputeSize — C++ SaveVisitorLambda::operator() with PrecomputeSize writer.\n");
        fprintf(f, "// Fields processed in SERIALIZE ORDER (same as C++ for_each over members).\n");
        fprintf(f, "// Returns end-offset of this object (C++ RelativeOffset).\n");
        fprintf(f, "func (m *%s) precomputeSize(ps *wire.PrecomputeSize) int {\n", typeName);

        // ONE loop over fields in serialize order — matching C++ SaveVisitorLambda::for_each.
        // C++ dispatches per field: dynamic_size → visitDynamicSize, nested → recurse,
        // scalar → inline (no cbs change), union_like → tag + optional recurse.
        for (auto& fd : fields) {
            if (fd.size == 0) continue; // Arena
            auto gn = fieldGoName(fd);
            switch (fd.kind) {
            case FieldKind::DynamicSize:
            case FieldKind::VectorLike:
                fprintf(f, "\tps.VisitDynamicSize(len(m.%s))\n", gn.c_str());
                break;
            case FieldKind::NestedStruct:
                if (fd.nestedGoType && fd.nestedGoType[0])
                    fprintf(f, "\tm.%s.precomputeSize(ps)\n", gn.c_str());
                break;
            case FieldKind::Optional:
                if (fd.nestedGoType && fd.nestedGoType[0]) {
                    fprintf(f, "\tif m.Has%s { m.%s.precomputeSize(ps) }\n", gn.c_str(), gn.c_str());
                } else {
                    fprintf(f, "\tif m.Has%s { ps.VisitDynamicSize(len(m.%s)) }\n", gn.c_str(), gn.c_str());
                }
                break;
            case FieldKind::Variant:
                // C++ union save (SaveAlternative, flat_buffers.h:837-855): reserve the
                // OUT-OF-LINE payload of the ACTIVE alternative. Scalar alt
                // (use_indirection==false, :848): a bare value at cbs+sizeof, no length
                // prefix → ps.Write(cbs+size). Vector-like alt (use_indirection==true,
                // :845): a count-prefixed vector → VisitDynamicSize. The 1-byte tag +
                // 4-byte reloff live INLINE in self (no payload), so tag 0 reserves nothing.
                fprintf(f, "\tswitch m.%sTag {\n", gn.c_str());
                for (size_t a = 0; a < fd.variantAlts.size(); a++) {
                    auto& alt = fd.variantAlts[a];
                    if (alt.kind == FieldKind::Scalar) {
                        fprintf(f, "\tcase %zu:\n\t\tps.Write(ps.CurrentBufferSize + %d)\n",
                                a + 1, alt.size);
                    } else {
                        fprintf(f, "\tcase %zu:\n\t\tps.VisitDynamicSize(len(m.%sAlt%zu))\n",
                                a + 1, gn.c_str(), a);
                    }
                }
                fprintf(f, "\t}\n");
                break;
            case FieldKind::VectorOfStruct:
                fprintf(f, "\t{\n");
                fprintf(f, "\t\tn := len(m.%s)\n", gn.c_str());
                fprintf(f, "\t\tif n > 0 {\n");
                fprintf(f, "\t\t\tself := ps.GetMessageWriter(n * 4)\n");
                fprintf(f, "\t\t\tfor i := 0; i < n; i++ { m.%s[i].precomputeSize(ps) }\n", gn.c_str());
                // C++ flat_buffers.h:1228: self.writeTo(writer, start - sizeof(uint32_t))
                // start = RightAlign(cbs + len, 4) + 4. Reloff array at start-4, count at start.
                fprintf(f, "\t\t\tstart := wire.RightAlign(ps.CurrentBufferSize+n*4, 4) + 4\n");
                fprintf(f, "\t\t\tps.Write(start) // count at start (4 bytes)\n");
                fprintf(f, "\t\t\tself.WriteToAt(ps, start - 4) // reloff array at start-4\n");
                fprintf(f, "\t\t} else { ps.VisitDynamicSize(0) }\n");
                fprintf(f, "\t}\n");
                break;
            case FieldKind::Scalar:
                break; // inline in object, no cbs change
            default:
                break;
            }
        }

        // Own object — GetMessageWriter + align + WriteToAt
        // C++ SaveVisitorLambda line 972: RightAlign(cbs + vtable[1] - 4, max(4, fb_align<Members>...)) + 4
        fprintf(f, "\t{ n := ps.GetMessageWriter(int(%sVTable[1])); ", typeName);
        fprintf(f, "n.WriteToAt(ps, wire.RightAlign(ps.CurrentBufferSize+int(%sVTable[1])-4, %d)+4) }\n",
                typeName, std::max(4, maxAlign));
        // Return end-offset of this object = current_buffer_size after WriteToAt
        fprintf(f, "\treturn ps.CurrentBufferSize\n");
        fprintf(f, "}\n\n");
    }

    // ---- writeToBuffer (Pass 2 of two-pass, C++ SaveVisitorLambda with WriteToBuffer) ----

    void emitWriteToBuffer(const char* typeName, const std::vector<FieldDesc>& fields, int maxAlign) {
        fprintf(f, "// writeToBuffer — C++ SaveVisitorLambda::operator() with WriteToBuffer writer.\n");
        fprintf(f, "// Fields in SERIALIZE ORDER (same as precomputeSize, same as C++ for_each).\n");
        fprintf(f, "// Returns selfStart (end-offset of this object) for parent's RelativeOffset.\n");
        fprintf(f, "func (m *%s) writeToBuffer(wb *wire.WriteToBuffer, vtableStart int, tmpl *wire.MessageTemplate) int {\n", typeName);

        // Collect scalar fields for later (written into self object).
        std::vector<const FieldDesc*> scalarFields;
        for (auto& fd : fields) {
            if (fd.size == 0) continue;
            if (fd.kind == FieldKind::Scalar) scalarFields.push_back(&fd);
        }

        // Declare result variables for all non-scalar fields (used for reloff patching).
        for (auto& fd : fields) {
            if (fd.size == 0) continue;
            auto gn = fieldGoName(fd);
            switch (fd.kind) {
            case FieldKind::DynamicSize:
            case FieldKind::VectorLike:
                fprintf(f, "\tvar %s int\n", (safeParam(gn) + "Off").c_str());
                break;
            case FieldKind::NestedStruct:
                if (fd.nestedGoType && fd.nestedGoType[0])
                    fprintf(f, "\tvar %s int\n", (safeParam(gn) + "Start").c_str());
                break;
            case FieldKind::Optional:
                fprintf(f, "\tvar %s int\n", (safeParam(gn) + "Off").c_str());
                break;
            case FieldKind::VectorOfStruct:
                fprintf(f, "\tvar %s int\n", (safeParam(gn) + "Off").c_str());
                break;
            case FieldKind::Variant:
                fprintf(f, "\tvar %s int\n", (safeParam(gn) + "Off").c_str());
                break;
            default: break;
            }
        }

        // ONE loop in serialize order — same as precomputeSize.
        for (auto& fd : fields) {
            if (fd.size == 0) continue;
            auto gn = fieldGoName(fd);
            switch (fd.kind) {
            case FieldKind::DynamicSize:
            case FieldKind::VectorLike:
                fprintf(f, "\t%s, _ = wb.VisitDynamicSize(m.%s)\n", (safeParam(gn) + "Off").c_str(), gn.c_str());
                break;
            case FieldKind::NestedStruct:
                if (fd.nestedGoType && fd.nestedGoType[0])
                    fprintf(f, "\t%s = m.%s.writeToBuffer(wb, vtableStart, tmpl)\n",
                            (safeParam(gn) + "Start").c_str(), gn.c_str());
                break;
            case FieldKind::Optional:
                if (fd.nestedGoType && fd.nestedGoType[0]) {
                    fprintf(f, "\tif m.Has%s { %s = m.%s.writeToBuffer(wb, vtableStart, tmpl) }\n",
                            gn.c_str(), (safeParam(gn) + "Off").c_str(), gn.c_str());
                } else {
                    fprintf(f, "\tif m.Has%s { %s, _ = wb.VisitDynamicSize(m.%s) }\n",
                            gn.c_str(), (safeParam(gn) + "Off").c_str(), gn.c_str());
                }
                break;
            case FieldKind::Variant:
                // C++ SaveAlternative (flat_buffers.h:837-855): write the ACTIVE
                // alternative's payload out-of-line and capture its RelativeOffset.
                fprintf(f, "\tswitch m.%sTag {\n", gn.c_str());
                for (size_t a = 0; a < fd.variantAlts.size(); a++) {
                    auto& alt = fd.variantAlts[a];
                    if (alt.kind == FieldKind::Scalar) {
                        // use_indirection==false (:848): write(&v, cbs+sizeof, sizeof);
                        // reloff = the new cbs. Bare LE scalar, no count prefix.
                        // (The lone scalar alternative in the schema is uint32/size 4.)
                        fprintf(f, "\tcase %zu:\n", a + 1);
                        fprintf(f, "\t\twb.WriteUint32(uint32(m.%sAlt%zu), wb.CurrentBufferSize+%d)\n",
                                gn.c_str(), a, alt.size);
                        fprintf(f, "\t\t%s = wb.CurrentBufferSize\n", (safeParam(gn) + "Off").c_str());
                    } else {
                        // use_indirection==true (:845-846): count-prefixed vector.
                        fprintf(f, "\tcase %zu:\n\t\t%s, _ = wb.VisitDynamicSize(m.%sAlt%zu)\n",
                                a + 1, (safeParam(gn) + "Off").c_str(), gn.c_str(), a);
                    }
                }
                fprintf(f, "\t}\n");
                break;
            case FieldKind::VectorOfStruct: {
                auto offVar = safeParam(gn) + "Off";
                fprintf(f, "\t{\n");
                fprintf(f, "\t\tn := len(m.%s)\n", gn.c_str());
                fprintf(f, "\t\tif n > 0 {\n");
                fprintf(f, "\t\t\tself := wb.GetMessageWriter(n*4, false)\n");
                fprintf(f, "\t\t\tfor i := 0; i < n; i++ {\n");
                fprintf(f, "\t\t\t\telemStart := m.%s[i].writeToBuffer(wb, vtableStart, tmpl)\n", gn.c_str());
                fprintf(f, "\t\t\t\tself.WriteRelativeOffset(elemStart, i*4)\n");
                fprintf(f, "\t\t\t}\n");
                // C++ flat_buffers.h:1227-1228: count at `start`, reloff array at `start-4`
                // self.FinalLocation = start - 4 (from precomputeSize)
                // Count goes at start = self.FinalLocation + 4
                fprintf(f, "\t\t\twb.WriteUint32(uint32(n), self.FinalLocation + 4)\n");
                fprintf(f, "\t\t\tself.WriteToAt(self.FinalLocation)\n");
                fprintf(f, "\t\t\t%s = wb.CurrentBufferSize\n", offVar.c_str());
                fprintf(f, "\t\t} else {\n");
                fprintf(f, "\t\t\t%s, _ = wb.VisitDynamicSize(nil)\n", offVar.c_str());
                fprintf(f, "\t\t}\n");
                fprintf(f, "\t}\n");
                break;
            }
            case FieldKind::Scalar:
                break; // written later into self object
            default:
                break;
            }
        }

        // Step 3: Own object — GetMessageWriter (zeroed), write fields, soffset, WriteToAt
        fprintf(f, "\tselfW := wb.GetMessageWriter(int(%sVTable[1]), true)\n", typeName);
        fprintf(f, "\tselfStart := selfW.FinalLocation\n");
        // vt is needed for scalars (field offset), OOL reloffs, and nested reloffs.
        bool needsVt = false;
        for (auto& fd : fields) {
            if (fd.size == 0) continue;
            if (fd.kind == FieldKind::Scalar || fd.kind == FieldKind::DynamicSize ||
                fd.kind == FieldKind::VectorLike || fd.kind == FieldKind::VectorOfStruct ||
                fd.kind == FieldKind::Optional || fd.kind == FieldKind::Variant ||
                (fd.kind == FieldKind::NestedStruct && fd.nestedGoType && fd.nestedGoType[0])) {
                needsVt = true; break;
            }
        }
        if (needsVt)
            fprintf(f, "\tvt := %sVTable\n", typeName);

        // Write soffset
        fprintf(f, "\t{ soff := int32(vtableStart - tmpl.VTableOffset(%sVTable) - selfStart); ", typeName);
        fprintf(f, "var b [4]byte; binary.LittleEndian.PutUint32(b[:], uint32(soff)); selfW.WriteScalar(b[:], 0) }\n");

        // Write scalars
        for (auto* fdp : scalarFields) {
            auto gn = fieldGoName(*fdp);
            auto slot = std::string(typeName) + "Slot" + gn;
            const char* goType = fdp->scalar.goType;

            if (strcmp(goType, "bool") == 0) {
                fprintf(f, "\tif m.%s { selfW.WriteScalar([]byte{1}, int(vt[%s+2])) }\n", gn.c_str(), slot.c_str());
            } else if (strcmp(goType, "uint8") == 0 || strcmp(goType, "int8") == 0) {
                fprintf(f, "\tselfW.WriteScalar([]byte{byte(m.%s)}, int(vt[%s+2]))\n", gn.c_str(), slot.c_str());
            } else if (strcmp(goType, "[16]byte") == 0) {
                fprintf(f, "\tselfW.WriteScalar(m.%s[:], int(vt[%s+2]))\n", gn.c_str(), slot.c_str());
            } else if (strcmp(goType, "uint32") == 0 || strcmp(goType, "int32") == 0) {
                fprintf(f, "\t{ var b [4]byte; binary.LittleEndian.PutUint32(b[:], uint32(m.%s)); selfW.WriteScalar(b[:], int(vt[%s+2])) }\n",
                        gn.c_str(), slot.c_str());
            } else if (strcmp(goType, "uint64") == 0 || strcmp(goType, "int64") == 0) {
                fprintf(f, "\t{ var b [8]byte; binary.LittleEndian.PutUint64(b[:], uint64(m.%s)); selfW.WriteScalar(b[:], int(vt[%s+2])) }\n",
                        gn.c_str(), slot.c_str());
            } else if (strcmp(goType, "uint16") == 0 || strcmp(goType, "int16") == 0) {
                fprintf(f, "\t{ var b [2]byte; binary.LittleEndian.PutUint16(b[:], uint16(m.%s)); selfW.WriteScalar(b[:], int(vt[%s+2])) }\n",
                        gn.c_str(), slot.c_str());
            } else if (strcmp(goType, "float64") == 0) {
                fprintf(f, "\t{ var b [8]byte; binary.LittleEndian.PutUint64(b[:], math.Float64bits(m.%s)); selfW.WriteScalar(b[:], int(vt[%s+2])) }\n",
                        gn.c_str(), slot.c_str());
            }
        }

        // Write RelativeOffsets for all non-scalar fields.
        // C++ self.write(&result, vtable[i++], sizeof(result)) per field.
        for (auto& fd : fields) {
            if (fd.size == 0) continue;
            auto gn = fieldGoName(fd);
            auto slot = std::string(typeName) + "Slot" + gn;
            switch (fd.kind) {
            case FieldKind::DynamicSize:
            case FieldKind::VectorLike:
                fprintf(f, "\tselfW.WriteRelativeOffset(%s, int(vt[%s+2]))\n",
                        (safeParam(gn) + "Off").c_str(), slot.c_str());
                break;
            case FieldKind::NestedStruct:
                if (fd.nestedGoType && fd.nestedGoType[0])
                    fprintf(f, "\tselfW.WriteRelativeOffset(%s, int(vt[%s+2]))\n",
                            (safeParam(gn) + "Start").c_str(), slot.c_str());
                break;
            case FieldKind::Optional:
                fprintf(f, "\tif m.Has%s {\n", gn.c_str());
                fprintf(f, "\t\tselfW.WriteScalar([]byte{1}, int(vt[%s+2]))\n", slot.c_str());
                fprintf(f, "\t\tselfW.WriteRelativeOffset(%s, int(vt[%s+1+2]))\n",
                        (safeParam(gn) + "Off").c_str(), slot.c_str());
                fprintf(f, "\t}\n");
                break;
            case FieldKind::Variant:
                // C++ union (flat_buffers.h:951,956): a 1-byte fb_type_tag at vtable slot
                // N, a 4-byte RelativeOffset at slot N+1. m.%sTag IS the fb tag (variant
                // index+1: 1=first alt, 2=second alt) — written verbatim, matching the
                // read side (ReadUint8). Tag 0 = empty/unset: self is already zeroed, so
                // both slots stay 0 (the `++i` empty arm, flat_buffers.h:957-959).
                fprintf(f, "\tif m.%sTag != 0 {\n", gn.c_str());
                fprintf(f, "\t\tselfW.WriteScalar([]byte{m.%sTag}, int(vt[%s+2]))\n",
                        gn.c_str(), slot.c_str());
                fprintf(f, "\t\tselfW.WriteRelativeOffset(%s, int(vt[%s+1+2]))\n",
                        (safeParam(gn) + "Off").c_str(), slot.c_str());
                fprintf(f, "\t}\n");
                break;
            case FieldKind::VectorOfStruct:
                // Unconditional: C++ SaveVisitorLambda writes the member's
                // RelativeOffset for EVERY dynamic member (flat_buffers.h:964)
                // — an empty vector points at the shared 4-byte empty blob
                // (save(VectorLike), flat_buffers.h:1212/1230), never slot 0.
                // The old `if len > 0` guard produced byte-divergent commits
                // (caught by TestGroundTruthMarshal on the empty-CR vectors).
                fprintf(f, "\tselfW.WriteRelativeOffset(%s, int(vt[%s+2]))\n",
                        (safeParam(gn) + "Off").c_str(), slot.c_str());
                break;
            default: break; // Scalar: already written inline
            }
        }

        fprintf(f, "\tselfW.WriteToAt(selfStart)\n");
        // Return selfStart for parent's RelativeOffset (C++ save_helper return)
        fprintf(f, "\treturn selfStart\n");
        fprintf(f, "}\n\n");
    }

    // ---- MarshalFDB — two-pass: PrecomputeSize + WriteToBuffer ----
    // Matches C++ detail::save (flat_buffers.h:1311) + save_with_vtables (flat_buffers.h:804).
    //
    // Pass 1 (PrecomputeSize): walk all fields, compute buffer size + record positions.
    // Pass 2 (WriteToBuffer): walk fields AGAIN in same order, write bytes at recorded positions.
    void emitMarshalFDB(const char* typeName, const std::vector<FieldDesc>& fields, int maxAlign) {
        fprintf(f, "func (m *%s) MarshalFDB() []byte {\n", typeName);
        fprintf(f, "\tt := %sTemplate\n", typeName);
        fprintf(f, "\tpackedVT := t.PackedVTables()\n\n");

        // === Pass 1: PrecomputeSize (C++ flat_buffers.h:1314) ===
        fprintf(f, "\t// Pass 1: PrecomputeSize\n");
        fprintf(f, "\tps := wire.NewPrecomputeSize()\n");
        fprintf(f, "\tvtNoop := ps.GetMessageWriter(len(packedVT))\n");
        fprintf(f, "\tm.precomputeSize(ps)\n");
        // FakeRoot (vtable {6,8,4}, objSize=8, maxAlign=4)
        fprintf(f, "\t{ n := ps.GetMessageWriter(8); n.WriteToAt(ps, wire.RightAlign(ps.CurrentBufferSize+4, 4)+4) }\n");
        fprintf(f, "\tvtNoop.WriteTo(ps)\n");
        fprintf(f, "\tvtableStart := ps.CurrentBufferSize\n");
        fprintf(f, "\t{ n := ps.GetMessageWriter(8); n.WriteToAt(ps, wire.RightAlign(ps.CurrentBufferSize+8, 8)) }\n");
        fprintf(f, "\ttotalSize := ps.CurrentBufferSize\n\n");

        // === Pass 2: WriteToBuffer (C++ flat_buffers.h:1319) ===
        fprintf(f, "\t// Pass 2: WriteToBuffer\n");
        fprintf(f, "\tbuf := make([]byte, totalSize)\n");
        fprintf(f, "\twb := wire.NewWriteToBuffer(buf, vtableStart, ps.WriteToOffsets)\n");
        fprintf(f, "\tvtW := wb.GetMessageWriter(len(packedVT), false)\n");
        fprintf(f, "\tvtW.WriteScalar(packedVT, 0)\n");
        fprintf(f, "\trootStart := m.writeToBuffer(wb, vtableStart, t)\n\n");

        // FakeRoot object
        fprintf(f, "\t// FakeRoot object\n");
        fprintf(f, "\tfakeRootW := wb.GetMessageWriter(8, true)\n");
        fprintf(f, "\tfakeRootStart := fakeRootW.FinalLocation\n");
        fprintf(f, "\tfakeRootW.WriteRelativeOffset(rootStart, int(wire.FakeRootVTable[2]))\n");
        fprintf(f, "\t{ soff := int32(vtableStart - t.VTableOffset(wire.FakeRootVTable) - fakeRootStart); ");
        fprintf(f, "var b [4]byte; binary.LittleEndian.PutUint32(b[:], uint32(soff)); fakeRootW.WriteScalar(b[:], 0) }\n");
        fprintf(f, "\tfakeRootW.WriteToAt(fakeRootStart)\n\n");

        // vtable + footer
        fprintf(f, "\tvtW.WriteTo()\n");
        fprintf(f, "\tfooterW := wb.GetMessageWriter(8, false)\n");
        fprintf(f, "\tfooterW.WriteRelativeOffset(fakeRootStart, 0)\n");
        fprintf(f, "\t{ var b [4]byte; binary.LittleEndian.PutUint32(b[:], %sFileID); footerW.WriteScalar(b[:], 4) }\n", typeName);
        fprintf(f, "\tfooterW.WriteToAt(wire.RightAlign(wb.CurrentBufferSize+8, 8))\n");

        // Release pooled objects before returning.
        fprintf(f, "\twire.ReleaseWriteToBuffer(wb)\n");
        fprintf(f, "\twire.ReleasePrecomputeSize(ps)\n");

        fprintf(f, "\treturn buf\n");
        fprintf(f, "}\n\n");

        // MarshalFDBPooled — same as MarshalFDB but reuses a caller-provided buffer.
        fprintf(f, "// MarshalFDBPooled is like MarshalFDB but reuses dst if capacity is sufficient.\n");
        fprintf(f, "func (m *%s) MarshalFDBPooled(dst []byte) []byte {\n", typeName);
        fprintf(f, "\tt := %sTemplate\n", typeName);
        fprintf(f, "\tpackedVT := t.PackedVTables()\n\n");

        fprintf(f, "\tps := wire.NewPrecomputeSize()\n");
        fprintf(f, "\tvtNoop := ps.GetMessageWriter(len(packedVT))\n");
        fprintf(f, "\tm.precomputeSize(ps)\n");
        fprintf(f, "\t{ n := ps.GetMessageWriter(8); n.WriteToAt(ps, wire.RightAlign(ps.CurrentBufferSize+4, 4)+4) }\n");
        fprintf(f, "\tvtNoop.WriteTo(ps)\n");
        fprintf(f, "\tvtableStart := ps.CurrentBufferSize\n");
        fprintf(f, "\t{ n := ps.GetMessageWriter(8); n.WriteToAt(ps, wire.RightAlign(ps.CurrentBufferSize+8, 8)) }\n");
        fprintf(f, "\ttotalSize := ps.CurrentBufferSize\n\n");

        fprintf(f, "\tvar buf []byte\n");
        fprintf(f, "\tif cap(dst) >= totalSize {\n");
        fprintf(f, "\t\tbuf = dst[:totalSize]\n");
        fprintf(f, "\t\tfor i := range buf { buf[i] = 0 }\n");
        fprintf(f, "\t} else {\n");
        fprintf(f, "\t\tbuf = make([]byte, totalSize)\n");
        fprintf(f, "\t}\n\n");

        fprintf(f, "\twb := wire.NewWriteToBuffer(buf, vtableStart, ps.WriteToOffsets)\n");
        fprintf(f, "\tvtW := wb.GetMessageWriter(len(packedVT), false)\n");
        fprintf(f, "\tvtW.WriteScalar(packedVT, 0)\n");
        fprintf(f, "\trootStart := m.writeToBuffer(wb, vtableStart, t)\n\n");

        fprintf(f, "\tfakeRootW := wb.GetMessageWriter(8, true)\n");
        fprintf(f, "\tfakeRootStart := fakeRootW.FinalLocation\n");
        fprintf(f, "\tfakeRootW.WriteRelativeOffset(rootStart, int(wire.FakeRootVTable[2]))\n");
        fprintf(f, "\t{ soff := int32(vtableStart - t.VTableOffset(wire.FakeRootVTable) - fakeRootStart); ");
        fprintf(f, "var b [4]byte; binary.LittleEndian.PutUint32(b[:], uint32(soff)); fakeRootW.WriteScalar(b[:], 0) }\n");
        fprintf(f, "\tfakeRootW.WriteToAt(fakeRootStart)\n\n");

        fprintf(f, "\tvtW.WriteTo()\n");
        fprintf(f, "\tfooterW := wb.GetMessageWriter(8, false)\n");
        fprintf(f, "\tfooterW.WriteRelativeOffset(fakeRootStart, 0)\n");
        fprintf(f, "\t{ var b [4]byte; binary.LittleEndian.PutUint32(b[:], %sFileID); footerW.WriteScalar(b[:], 4) }\n", typeName);
        fprintf(f, "\tfooterW.WriteToAt(wire.RightAlign(wb.CurrentBufferSize+8, 8))\n");

        fprintf(f, "\twire.ReleaseWriteToBuffer(wb)\n");
        fprintf(f, "\twire.ReleasePrecomputeSize(ps)\n");

        fprintf(f, "\treturn buf\n");
        fprintf(f, "}\n\n");
    }

    // ---- FlatBuffers vector parser (same as v4) ----

    void emitFBVectorParser(const char* typeName) {
        fprintf(f, "// Parse%sVectorFromReader reads a FlatBuffers vector of %s.\n", typeName, typeName);
        fprintf(f, "func Parse%sVectorFromReader(r *wire.Reader, slot int) []%s {\n", typeName, typeName);
        fprintf(f, "\tcount, err := r.ReadVectorCount(slot)\n");
        fprintf(f, "\tif err != nil || count == 0 { return nil }\n");
        fprintf(f, "\tresult := make([]%s, 0, count)\n", typeName);
        fprintf(f, "\tfor i := 0; i < count; i++ {\n");
        fprintf(f, "\t\telemR, err := r.ReadVectorElementReader(slot, i)\n");
        fprintf(f, "\t\tif err != nil { continue }\n");
        fprintf(f, "\t\tvar elem %s\n", typeName);
        fprintf(f, "\t\telem.UnmarshalFromReader(elemR)\n");
        fprintf(f, "\t\tresult = append(result, elem)\n");
        fprintf(f, "\t}\n");
        fprintf(f, "\treturn result\n");
        fprintf(f, "}\n\n");
    }

    // ---- String vector parser (sub-slice instead of make+copy) ----

    void emitStringVectorParser(const char* typeName, const std::vector<FieldDesc>& fields) {
        fprintf(f, "// Parse%sStringVector decodes a VectorRef<%s, VecSerStrategy::String>.\n", typeName, typeName);
        fprintf(f, "// Each element's DynamicSize fields are inline: [len(4)][data] per field.\n");
        fprintf(f, "func Parse%sStringVector(data []byte) []%s {\n", typeName, typeName);
        int nDynFields = 0;
        for (auto& fd : fields)
            if (fd.size > 0 && fd.kind == FieldKind::DynamicSize) nDynFields++;
        fprintf(f, "\tif len(data) < 4 { return nil }\n");
        fprintf(f, "\tcount := binary.LittleEndian.Uint32(data[0:4])\n");
        fprintf(f, "\tif count == 0 { return nil }\n");
        fprintf(f, "\t// Cap allocation to prevent OOM from crafted count values.\n");
        fprintf(f, "\t// Minimum %d bytes per element (4-byte length prefix per field).\n", 4 * nDynFields);
        fprintf(f, "\tmaxCount := uint32((len(data) - 4) / %d)\n", 4 * nDynFields);
        fprintf(f, "\tif maxCount == 0 { maxCount = 1 }\n");
        fprintf(f, "\tallocCount := count\n");
        fprintf(f, "\tif allocCount > maxCount { allocCount = maxCount }\n");
        fprintf(f, "\tpos := 4\n");
        fprintf(f, "\tresult := make([]%s, 0, allocCount)\n", typeName);
        fprintf(f, "\tfor i := uint32(0); i < count; i++ {\n");
        fprintf(f, "\t\tvar elem %s\n", typeName);
        for (auto& fd : fields) {
            if (fd.size == 0 || fd.kind != FieldKind::DynamicSize) continue;
            auto gn = fieldGoName(fd);
            fprintf(f, "\t\tif pos+4 > len(data) { break }\n");
            fprintf(f, "\t\t{\n");
            fprintf(f, "\t\t\tn := int(binary.LittleEndian.Uint32(data[pos:]))\n");
            fprintf(f, "\t\t\tpos += 4\n");
            fprintf(f, "\t\t\tif n < 0 || pos+n > len(data) { break }\n");
            fprintf(f, "\t\t\telem.%s = data[pos:pos+n:pos+n]\n", gn.c_str());
            fprintf(f, "\t\t\tpos += n\n");
            fprintf(f, "\t\t}\n");
        }
        if (strcmp(typeName, "KeyRangeRef") == 0) {
            fprintf(f, "\t\t// Apply the same single-key-optimization inversion as\n");
            fprintf(f, "\t\t// UnmarshalFromReader (see keyrangeref_custom.go). Without this, a\n");
            fprintf(f, "\t\t// vector element written by C++'s optimization (begin+\\x00 == end →\n");
            fprintf(f, "\t\t// emit (end, empty)) would parse with Begin/End swapped.\n");
            fprintf(f, "\t\t// Note: after inversion, elem.Begin and elem.End share the same\n");
            fprintf(f, "\t\t// backing array (the original wire buffer). Same zero-copy\n");
            fprintf(f, "\t\t// convention as the rest of this function.\n");
            fprintf(f, "\t\tif len(elem.End) == 0 && len(elem.Begin) > 0 {\n");
            fprintf(f, "\t\t\tfirst := elem.Begin\n");
            fprintf(f, "\t\t\telem.Begin = first[:len(first)-1]\n");
            fprintf(f, "\t\t\telem.End = first\n");
            fprintf(f, "\t\t}\n");
        }
        fprintf(f, "\t\tresult = append(result, elem)\n");
        fprintf(f, "\t}\n");
        fprintf(f, "\treturn result\n");
        fprintf(f, "}\n\n");
    }
};

// ============================================================
// extractType — uses GoEmitterV5
// ============================================================

template <class T, bool SkipObjectWriter = false>
void extractType(const char* outDir, const char* name) {
    int pipefd[2];
    pipe(pipefd);
    pid_t pid = fork();
    if (pid == 0) {
        close(pipefd[0]);

        // Collect fields.
        FieldCollector<T> collector;
        T msg{};
        if constexpr (serializable_traits<T>::value)
            serializable_traits<T>::serialize(collector, msg);
        else
            msg.serialize(collector);

        // Get authoritative vtable closure from C++ get_vtableset (flat_buffers.h:796).
        // This traverses the FULL TYPE TREE via InsertVTableLambda, collecting vtables
        // for ALL reachable types including absent Optional inner types.
        // Previously we extracted from serialized bytes which MISSED absent Optionals.
        std::vector<std::vector<uint16_t>> closure;
        std::vector<uint16_t> rootVTable;
        if constexpr (!SkipObjectWriter && requires { T::file_identifier; }) {
            // Use get_vtableset for the complete vtable closure.
            struct MinimalContext {
                MinimalContext& context() { return *this; }
                ProtocolVersion protocolVersion() const { return currentProtocolVersion(); }
                void addArena(Arena&) {}
            };
            MinimalContext ctx;
            auto root = detail::fake_root(msg);
            const auto* vts = detail::get_vtableset(root, ctx);

            // Convert VTableSet to our closure format.
            for (auto& [vt, off] : vts->offsets) {
                std::vector<uint16_t> entries(vt->begin(), vt->end());
                closure.push_back(entries);
            }

            // Root vtable from the same set (the vtable with the most entries
            // is typically the root, but we can also extract from serialized bytes).
            ObjectWriter wr(IncludeVersion(currentProtocolVersion()));
            wr.serialize(T::file_identifier, msg);
            auto bytes = wr.toStringRef();
            rootVTable = extractMessageVTable(bytes.begin(), bytes.size());
        }
        auto& vt = rootVTable.empty() ? collector.vtable : rootVTable;

        // Emit.
        FILE* pf = fdopen(pipefd[1], "w");
        GoEmitterV5 emitter{pf};
        emitter.emitFull(name, collector.fields, vt, getFileId<T>(), closure);
        fclose(pf);
        _exit(0);
    }

    close(pipefd[1]);
    std::string lowerName = toLower(name);
    std::string path = std::string(outDir) + "/" + lowerName + "_generated.go";
    FILE* out = fopen(path.c_str(), "w");
    if (!out) { perror(path.c_str()); goto wait; }
    {
        char buf[4096];
        ssize_t n;
        while ((n = read(pipefd[0], buf, sizeof(buf))) > 0)
            fwrite(buf, 1, n, out);
        fclose(out);
    }

wait:
    close(pipefd[0]);
    int status;
    waitpid(pid, &status, 0);
    if (WIFEXITED(status) && WEXITSTATUS(status) == 0)
        fprintf(stderr, "OK %s → %s_generated.go\n", name, lowerName.c_str());
    else
        fprintf(stderr, "SKIP %s\n", name);
}

